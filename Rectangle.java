
public class Rectangle 
{
    private final int rows;
    
    private final int columns;
    
    public Rectangle(int rows, int columns)
    {
        this.rows = rows;
        this.columns = columns;
    }
    
    public int getRows()
    {
        return rows;
    }
    
    public int getColumns()
    {
        return columns;
    }
}
